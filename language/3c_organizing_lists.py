# Sort Lists Alphabetically

cars = ['toyota', 'bmw', 'subaru', 'audi']
print(cars)
cars.sort()
print(cars)

# Sort Lists in Reverse Alphabetical Order
cars.sort(reverse=True)
print(cars)

# Use Sorted List as Value - No Change to original List
colors = ['red', 'blue', 'yellow', 'black', 'green', 'purple']
print(sorted(colors))
print(colors)

# Print a List in Reverse Order
colors.reverse()
print(colors)

# Finding the Length of a List
print(len(colors))